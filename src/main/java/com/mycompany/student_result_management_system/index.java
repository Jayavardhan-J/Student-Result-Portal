        String course ="B.E";
        String branch=(String)BranchDropDown.getSelectedItem();
        String rollNo =rollNumberField.getText();
        String name = nameField.getText();
        String gender=(String)genderDropDown.getSelectedItem();
        String fatherName=fNameField.getText();
        
        
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Vardhan123");
            
            Statement st=con.createStatement();
            st.executeUpdate("insert into student(rollno,course,Branch,name,gender,fathername)values('"+rollNo+"','"+course+"','"+branch+"','"+name+"','"+gender+"','"+fatherName+"')");
            JOptionPane.showMessageDialog(null,"Data added successfully!");
            setVisible(false);
            new index().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
