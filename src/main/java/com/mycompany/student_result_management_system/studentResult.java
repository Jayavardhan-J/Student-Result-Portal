    public void showResult(){
        try{
            
            
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Vardhan123");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student join "+bName+"_markscard on student.rollNo='"+rollno+"' and "+bName+"_markscard.rollno='"+rollno+"'");
            rs.next();
            ResultSetMetaData RSMD =rs.getMetaData();
            String subject_1=RSMD.getColumnName(9);
            String subject_2=RSMD.getColumnName(10);
            String subject_3=RSMD.getColumnName(11);
            String subject_4=RSMD.getColumnName(12);
            String subject_5=RSMD.getColumnName(13);
            String subject_6=RSMD.getColumnName(14);
            
            rollNoField.setText(rollno);
            nameField.setText(rs.getString("name"));
            branchField.setText(bName);
            genderField.setText(rs.getString("gender"));
            fNameField.setText(rs.getString("fathername"));
            subject1.setText(subject_1);
            subject2.setText(subject_2);
            subject3.setText(subject_3);
            subject4.setText(subject_4);
            subject5.setText(subject_5);
            subject6.setText(subject_6);
            result1.setText(rs.getString(subject_1));
            result2.setText(rs.getString(subject_2));
            result3.setText(rs.getString(subject_3));
            result4.setText(rs.getString(subject_4));
            result5.setText(rs.getString(subject_5));
            result6.setText(rs.getString(subject_6));
            
            int r1=Integer.parseInt(rs.getString(subject_1));
            int r2=Integer.parseInt(rs.getString(subject_2));
            int r3=Integer.parseInt(rs.getString(subject_3));
            int r4=Integer.parseInt(rs.getString(subject_4));
            int r5=Integer.parseInt(rs.getString(subject_5));
            int r6=Integer.parseInt(rs.getString(subject_6));
            if(r1<35 || r2<35 || r3<35 || r4<35 || r5<35 || r6<36){
                ImageIcon img = new ImageIcon("D:/Java project/NetBeans/Student_result_management_system/Assets/fail.png");
                resultIcon.setIcon(img);
            }
            int tot=r1+r2+r3+r4+r5+r6;
            totField.setText(tot+"/600");
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }
