    public void showTable(){
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Vardhan123");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("(select * from cse_markscard) union (select * from ise_markscard) union (select * from me_markscard)union (select * from eee_markscard) union (select * from ece_markscard) union (select * from bt_markscard)");
            
            ResultSetMetaData RSMD= rs.getMetaData();
            int colCount=RSMD.getColumnCount();
            DefaultTableModel DFT=(DefaultTableModel) resultTable.getModel();
            DFT.setRowCount(0);
            String subject_1=RSMD.getColumnName(3);
            String subject_2=RSMD.getColumnName(4);
            String subject_3=RSMD.getColumnName(5);
            String subject_4=RSMD.getColumnName(6);
            String subject_5=RSMD.getColumnName(7);
            String subject_6=RSMD.getColumnName(8);
           while(rs.next())
            {
            Vector vec=new Vector();
            for(int i=1;i<=colCount;i++)
            {
                vec.add(rs.getString("rollno"));
                vec.add(rs.getString("name"));
                vec.add(rs.getString(subject_1));
                vec.add(rs.getString(subject_2));
                vec.add(rs.getString(subject_3));
                vec.add(rs.getString(subject_4));
                vec.add(rs.getString(subject_5));
                vec.add(rs.getString(subject_6));
            }
            DFT.addRow(vec);
        }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }
