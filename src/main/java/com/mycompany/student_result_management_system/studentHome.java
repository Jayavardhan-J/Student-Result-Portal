    private void search_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_search_btnActionPerformed
        String rollNo = rollno_field.getText();
        String branch = (String)branch_dropDown.getSelectedItem();
        String bName=branch+"_markscard";
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Vardhan123");
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select * from student inner join "+bName+" where student.rollNo='"+rollNo+"' and "+bName+".rollNo='"+rollNo+"'");
            if(rs.next()){
                setVisible(false);
                new studentResult(rollNo,branch).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Incorrect Roll Number!!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, e.toString());
        }
    }//GEN-LAST:event_search_btnActionPerformed

